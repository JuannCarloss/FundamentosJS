const escola = "cod3r"
//charAt sempre começa contando do 0
console.log (escola.charAt(4))
console.log (escola.charAt(0))

//indexOf te da apenas o caracter do numero escolhido
console.log (escola.indexOf(3))

//substring mostra a partir do numero escolhido
console.log (escola.substring(1))

//concat soma strings, sendo eles literais ou variáveis
console.log('Escola '.concat(escola).concat('!!'))

//replace troca o caracter do numero a sua escolha
console.log (escola.replace(3, 'e'))